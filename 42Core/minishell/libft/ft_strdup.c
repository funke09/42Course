#include "libft.h"

char	*ft_strdup(char const *s1)
{
	int		i;
	size_t	l;
	char	*s2;

	i = 0;
	l = ft_strlen(s1) + 1;
	s2 = (char *)malloc(sizeof(char) * l);
	if (!s2)
		return (NULL);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
