#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac > 1)
	{
		i = 0;
		while (++i != ac)
		{
			while (*av[i] != '\0')
				write(1, av[i]++, 1);
			write(1, "\n", 1);
		}
	}
	return (0);
}
