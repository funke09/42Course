#include <unistd.h>

int	main(int ac, char **av)
{
	int	index;

	if (ac > 1)
	{
		index = ac;
		while (--index != 0)
		{
			while (*av[index] != '\0')
				write(1, av[index]++, 1);
			write(1, "\n", 1);
		}
	}
	return (0);
}
