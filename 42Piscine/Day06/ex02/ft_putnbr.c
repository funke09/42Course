#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	helper;

	if (nb == -2147483648)
	{
		ft_putnbr(-2);
		ft_putnbr(147483648);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	helper = 48 + nb % 10;
	write (1, &helper, 1);
}
