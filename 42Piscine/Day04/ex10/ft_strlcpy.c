unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	index;
	unsigned int	src_length;

	index = 0;
	src_length = 0;
	while (src[src_length] != '\0')
		src_length++;
	if (size)
	{
		while (src[index] != '\0' && index < size - 1)
		{
			dest[index] = src[index];
			index++;
		}
		dest[index] = '\0';
	}
	return (src_length);
}
